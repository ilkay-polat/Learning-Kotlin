package fundamentals.variable

fun main() {
    val num1: Int = 3
    val num2 = 4


    println("num1: $num1")
    println("num2: $num2")

    var num3 = 4.0
    println("num3: $num3")
    num3 = 5.0
    println("num3: $num3")


    val isTrue: Boolean = true
    val isFalse = false

    println("isTrue: $isTrue")
    println("isFalse: $isFalse")

    val name: String = "Ilkay"
    println("name: $name")

}